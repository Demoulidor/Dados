								<h2>O que é o SITRANSP?</h2>
								<p class="font2">Indicadores de Transparência Parlamentar</p>
								<p>O <strong>Sistema do Índice de Transparência Parlamentar</strong>, que visa avaliar o quanto uma câmara de parlamentares ou mesmo o poder executivo é realmente transparente e procura oferecer aos cidadãos todas as informações públicas.</p>
                                <p>O Sistema do Índice de Transparência Parlamentar tem como objetivo medir o nível de transparência que os deputados possuem. E um dos pontos principais é saber o quanto os políticos estão dispostos a serem fiscalizados pela sociedade. Isso servirá para definir para o parlamentar quanto a transparência parlamentar é sinal de um mandato responsável, atento aos interesses da sociedade e verdadeiramente representativo de seus eleitores. Um verdadeiro mandato cidadão, como a gente chama aqui na Voz do Cidadão.</p>
                                <p>O Índice de Transparência avalia sete quesitos principais: atuação do parlamentar direta, atuação do parlamentar indireta, governança do mandato, controle social, acessibilidade do site na internet, atuação nas mídias sociais e processo eleitoral. Dá para perceber que o envolvimento do parlamentar com as ferramentas da web é cada vez mais importante e indicativo de que ele está disposto a tornar seu mandato transparente e voltado aos cidadãos eleitores.</p>
                              <p>O <strong>SITRANSP</strong> pode ser utilizado por qualquer cidadão, parlamentar ou entidade que deseja saber quanto uma casa legislativa ou seus parlamentares são realmente transparentes, ou seja, o que o parlamentar executa no seu mandato.</p>
                                <p>O <strong>SITRANSP</strong> gera um ranking dos parlamentares da casa legislativa, forçando-o diretamente a uma disputa saudável e bastante benéfica à sociedade entre outros parlamentares da casa legislativa. Pois a cada vez que ele demonstrar sua prestação de contas no <strong>Sistema</strong> ele terá sua pontuação elevada e assim cada vez mais terá seu nome entre as primeiras colocações, e assim em consequência, poderá explorar de forma como atender aos seus preceitos ou até para promover marketing do seu mandato.</p>
